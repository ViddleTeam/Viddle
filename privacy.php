2. Jakie zbieramy dane, w jaki sposób je zbieramy oraz jak są one wykorzystywane?

Dane, które zbieramy od zarejestrowanych to jedynie Twój adres mailowy. Wykorzystujemy zebrane dane do świadczenia usługi i/lub ich ulepszania, tj.: weryfikacja, czy adres mailowy należy do Ciebie, kontaktu drogą elektroniczną, do celów reklamowych i działań marketingowych. Chcemy się upewnić, że przestrzegamy wymogów prawnych.

Dane, które uzyskujemy od odwiedzających witrynę to typ urządzenia i przeglądarka, których używacie, adres IP oraz informacje o plikach cookie zainstalowanych na urządzeniu.

Zebrane informacje są wykorzystywane w celach, gdy wykonujesz działanie, tj. rejestracja nowego konta.

3. Jak długo przechowujemy zebrane dane osobowe?

Dane osobowe są przechowywane tak długo, jak to jest potrzebne do świadczenia usługi i przestrzegania zobowiązań prawnych.

Masz prawo do usunięcia swoich danych całkowicie, lecz wtedy będziemy musieli zamknąć Twoje konto na Viddle.

4. Jak uzyskać dostęp do danych osobowych i nimi zarządzać?

Do przekazanych danych możesz mieć wgląd poprzez wysłanie żądania danych na adres mailowy support@viddle.xyz. Możesz poprosić o ich modyfikację, nanosić poprawki, ograniczać przetwarzanie lub sprzeciwić się ich przetwarzaniu w przypadkach, gdy zostałeś/aś poproszony/a o zgodę na ich przetwarzanie.

Istnieje wiele przypadków, w których te prawa mogą być ograniczone i/lub w których możemy zachować dane po wycofaniu zgody.

Jeżeli jednak masz wątpliwości, możesz się z nami skontaktować drogą elektroniczną (adres mailowy: support@viddle.xyz) bądź przez komunikator Discord, otwierając ticket na naszym serwerze.


Viddle jest portalem przeznaczonym dla osób powyżej 13 lat. Żadna osoba, która nie przekracza progu dozwolonego wieku, nie może posiadać konta w serwisie. Nie zbieramy żadnych danych od osób małoletnich, chyba że rodzic/opiekun prawny wyraził na to zgodę. W przypadku, gdy dowiemy się, że zasada została naruszona, konto zostanie usunięte wraz ze wszystkimi informacjami. Jeżeli jesteś rodzicem lub opiekunem prawnym, a chciałbyś zarządzać kanałem należącym do osoby małoletniej, skontaktuj się z nami poprzez drogę elektroniczną (support@viddle.xyz) lub poprzez serwer Discord, otwierając ticket.


Jeżeli wyrazisz zgodę na przetwarzanie Twoich danych, będziemy je przetwarzać zgodnie z ogólnym rozporządzeniem o ochronie danych osobowych, RODO (GDPR) oraz przepisach dotyczących ochrony danych obowiązujących na terytorium Rzeczypospolitej Polskiej.


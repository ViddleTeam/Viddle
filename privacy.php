Posiadamy profile w głównych mediach społecznościowych, tj. Facebook, Twitter czy Instagram. Prosimy również nie zapominać o tym, że wszystkie informacje podane do obszarów publicznych tychże sieci mogą być łatwo odczytane, zebrane bądź zapisane przez innych użytkowników stron trzecich. Nie jesteśmy w stanie i nie możemy monitorować działań zarówno innych platform, jak i również ich użytkowników.

Klikając na link w opisie np. filmu, zostaniesz przekierowany na stronę należącą do osoby/firmy trzeciej. Nie możemy jednak kontrolować działań wykonywanych przez tamtejszą witrynę, w związku z tym nie ponosimy odpowiedzialności za wykorzystanie danych osobowych przez inną stronę. Dla pewności, zapoznaj się z polityką prywatności strony internetowej, na którą przechodzisz z poziomu Viddle.

